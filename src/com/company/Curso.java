package com.company;

public class Curso {

    private String nombre;
    private  Integer curso;

    /**
     * Creo los getters y setters para el curso
     * @return
     */

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Integer getCurso() {
        return curso;
    }

    public void setCurso(Integer curso) {
        this.curso = curso;
    }
}


